    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarActionPerformed
        // TODO add your handling code here:

        System.out.println(passwordPasswordField.getPassword());
        System.out.println(passwordPasswordField1.getPassword());
        
        System.out.println(String.valueOf(passwordPasswordField1.getPassword()));
        System.out.println(String.valueOf(passwordPasswordField.getPassword()));
        if (!validaVentanaRE()) {
            if (!String.valueOf(passwordPasswordField.getPassword()).equalsIgnoreCase(String.valueOf(passwordPasswordField1.getPassword()))) {
                JOptionPane.showMessageDialog(RegistroEstudiante.this, "El password debe coincidir");
            }else 
                if (String.valueOf(passwordPasswordField.getPassword()).length() < 5) {
                JOptionPane.showMessageDialog(null, "El password debe poseer más de 5 caracteres");
            }
            else {
                if (!controlusuario.UsuariosExistentes().contains(txtusername.getText())) {
                    Rol auxrol = controlrol.findByName((String) jComboBox1.getSelectedItem());
                    Usuario usua = new Usuario();
                    usua.setNombUsuario(txtusername.getText());
                    usua.setNombre(txtnombre.getText());
                    usua.setApellido1er(txtApellido1.getText());
                    usua.setApellido2do(txtaApellido2.getText());
                    usua.setRolIdrol(auxrol);
                    usua.setContraseña(Crypto.getEncryptedSHAString(String.valueOf(passwordPasswordField.getPassword())));
                    controlusuario.create(usua);
                    
                    Principal.usuarioAutentificado = usua;
                    AutentificacionInicialVisual.usuarioAutentificado = usua;
                    
                    usuario = usua;
                    cleanComponen();
                    
                    Principal.autenticado = true;
                    Principal.spprincipal.setVisible(true);
                    Principal.btnautenticar.setVisible(false);
                    Principal.btnsalir.setVisible(true);
                    
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Debe cambiar el nombre de usuario debido a que el proporcionado ya existe");
                    txtusername.setText("");
                }
            }
            
            System.out.println("este es " + (String) jComboBox1.getSelectedItem());
        }
    }//GEN-LAST:event_btninsertarActionPerformed

