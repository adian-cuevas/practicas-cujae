    private boolean validaVentanaRE() {
        boolean valor = false;
        if (txtusername.getText().isEmpty() || txtApellido1.getText().isEmpty() || txtnombre.getText().isEmpty()
                || txtaApellido2.getText().isEmpty() || passwordPasswordField.getPassword().length == 0 || passwordPasswordField1.getPassword().length == 0) {
            valor = true;
            JOptionPane.showMessageDialog(null, "Debe completar todos los elementos que se le piden");
        }
        return valor;
    }

